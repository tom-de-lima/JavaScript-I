let num1 = prompt("Digite o primeiro número: ");
let num2 = prompt("Digite o segundo número: ");

let soma = parseFloat(num1) + parseFloat(num2); // "parse" objeto usado para conversão de valores de uma variável

let subtracao = parseFloat(num1) - parseFloat(num2);

let multiplicacao = parseFloat(num1) * parseFloat(num2);

let divisao = parseFloat(num1) / parseFloat(num2);



alert(
    "\nCadastro realizado com sucesso!" + " " + "\nSomatório: " + soma + " " + "\nSubtração: " + subtracao + " " + "\nMultiplicação: " + multiplicacao + " " + "\nDivisão: " + divisao
)

